# use 10^s == 1 + 2.3025*s


s = 1/1024
_10_is = (1,s*2.3025)
print(f"s={s:.10f}, 10^{s:.10}i={_10_is[0]:.10f} + {_10_is[1]:.10f}i")

for i in range(10):
    s = s*2
    _10_is = (_10_is[0]**2 -_10_is[1]**2, 2*_10_is[0]*_10_is[1])
    print(f"s={s:.10f}, 10^{s:.10}i={_10_is[0]:.10f} + {_10_is[1]:.10f}i")

